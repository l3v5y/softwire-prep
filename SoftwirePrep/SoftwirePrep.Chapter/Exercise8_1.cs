namespace SoftwirePrep.Exercises
{
    public class Exercise8_1
    {
        /// <summary>
        /// Triples an int
        /// </summary>
        /// <param name="x"></param>
        /// <returns></returns>
        public static int Triple(int x)
        {
            return x * 3;
        }

        /// <summary>
        /// Triples a float
        /// </summary>
        /// <param name="x"></param>
        /// <returns></returns>
        public static float Triple(float x)
        {
            return x * 3;
        }
    }
}
